"""US-002: Slice-0 OncoTree->SNOMED gold set + mapping eval harness.

Ships **no resolver** — only the gold-set artifact loader and the §1.5(f)
scoring harness. ``score()`` reports the confusion matrix at three
granularities (distinct-code, row-weighted, per-frequency-bucket).

The gold-set artifact (``tests/data/gold/oncotree_condition_slice0.jsonl``)
carries one row per distinct observed ``ONCOTREE_CODE``. Its ``gold_concept_id``
/ ``gold_label`` are an **external oracle**: hand-labelled by a human (or a
trusted crosswalk), **never** generated by Sema's own resolver (US-006), else
US-012 would grade its own homework. Ralph scaffolds the file and fills
``row_count`` / ``notes``; codes awaiting the human gate are ``UNLABELLED`` and
excluded from scoring but surfaced via :meth:`GoldSet.unlabelled_codes`.

Distinct-code enumeration query (documented per US-002), run against
``~/.sema/poc.duckdb``::

    SELECT ONCOTREE_CODE AS code, COUNT(*) AS row_count
    FROM (
      SELECT ONCOTREE_CODE FROM cbioportal_<study>.sample
      ...UNION ALL across every loaded cbioportal_* study...
    )
    WHERE ONCOTREE_CODE IS NOT NULL AND TRIM(ONCOTREE_CODE) <> ''
    GROUP BY 1 ORDER BY row_count DESC, code
"""

from __future__ import annotations

import json
from dataclasses import dataclass, field
from pathlib import Path
from typing import Any, Iterable, Protocol

from sema.eval.mapping_goldset_utils import (
    ConfusionMatrix,
    Decision,
    GoldLabel,
    GoldRow,
    classify_cell,
    derive_zone,
    frequency_bucket,
)


class _Cursor(Protocol):
    def execute(self, sql: str) -> Any: ...
    def fetchall(self) -> list[Any]: ...


def load_gold_set(path: str | Path) -> list[GoldRow]:
    """Load the JSONL gold-set artifact into typed rows."""
    rows: list[GoldRow] = []
    for line in Path(path).read_text(encoding="utf-8").splitlines():
        line = line.strip()
        if not line or line.startswith("#"):
            continue
        rows.append(_row_from_json(json.loads(line)))
    return rows


def _row_from_json(obj: dict[str, Any]) -> GoldRow:
    concept = obj.get("gold_concept_id")
    return GoldRow(
        oncotree_code=str(obj["oncotree_code"]),
        gold_concept_id=None if concept is None else int(concept),
        gold_label=GoldLabel(obj["gold_label"]),
        row_count=int(obj["row_count"]),
        notes=str(obj.get("notes", "")),
    )


@dataclass
class GoldSet:
    """A loaded gold set with labelled/unlabelled accounting (the human gate)."""

    rows: list[GoldRow]

    def by_code(self) -> dict[str, GoldRow]:
        return {r.oncotree_code: r for r in self.rows}

    @property
    def labelled_count(self) -> int:
        return sum(1 for r in self.rows if r.gold_label is not GoldLabel.UNLABELLED)

    def labelled_rows(self) -> list[GoldRow]:
        return [r for r in self.rows if r.gold_label is not GoldLabel.UNLABELLED]

    def unlabelled_codes(self) -> list[str]:
        return [
            r.oncotree_code
            for r in self.rows
            if r.gold_label is GoldLabel.UNLABELLED
        ]

    def coverage_fraction(self) -> float:
        if not self.rows:
            return 0.0
        return self.labelled_count / len(self.rows)


@dataclass
class GoldSetReport:
    """§1.5(f) metrics at three granularities + scoring accounting."""

    distinct_code: ConfusionMatrix
    row_weighted: ConfusionMatrix
    per_bucket: dict[str, ConfusionMatrix]
    scored_codes: int = 0
    unscored_unlabelled: list[str] = field(default_factory=list)
    decisions_without_gold: list[str] = field(default_factory=list)
    labelled_without_decision: list[str] = field(default_factory=list)

    def as_dict(self) -> dict[str, Any]:
        return {
            "distinct_code": self.distinct_code.as_dict(),
            "row_weighted": self.row_weighted.as_dict(),
            "per_bucket": {k: v.as_dict() for k, v in self.per_bucket.items()},
            "scored_codes": self.scored_codes,
            "unscored_unlabelled": self.unscored_unlabelled,
            "decisions_without_gold": self.decisions_without_gold,
            "labelled_without_decision": self.labelled_without_decision,
        }


def score(
    gold_rows: Iterable[GoldRow],
    decisions: Iterable[Decision],
) -> GoldSetReport:
    """Score predicted decisions against the gold set per §1.5(f).

    Unit of evaluation = distinct source code. ``UNLABELLED`` gold rows are
    excluded (never scored) and surfaced in ``unscored_unlabelled``.
    """
    gold_by_code = {g.oncotree_code: g for g in gold_rows}
    dec_by_code = {d.source_code: d for d in decisions}

    report = GoldSetReport(
        distinct_code=ConfusionMatrix(),
        row_weighted=ConfusionMatrix(),
        per_bucket={},
    )
    report.decisions_without_gold = sorted(
        set(dec_by_code) - set(gold_by_code)
    )

    for code, gold in gold_by_code.items():
        if gold.gold_label is GoldLabel.UNLABELLED:
            report.unscored_unlabelled.append(code)
            continue
        decision = dec_by_code.get(code)
        if decision is None:
            report.labelled_without_decision.append(code)
            continue
        _accumulate(report, gold, decision)
        report.scored_codes += 1

    report.unscored_unlabelled.sort()
    report.labelled_without_decision.sort()
    return report


def _accumulate(
    report: GoldSetReport,
    gold: GoldRow,
    decision: Decision,
) -> None:
    zone = derive_zone(
        decision.status, decision.resolution_status, decision.concept_id
    )
    cell = classify_cell(gold, zone, decision.concept_id)
    report.distinct_code.add(cell, 1.0)
    report.row_weighted.add(cell, float(gold.row_count))
    bucket = frequency_bucket(gold.row_count)
    report.per_bucket.setdefault(bucket, ConfusionMatrix()).add(cell, 1.0)


# --- live distinct-code enumeration (integration / scaffolding) -------------

_DISTINCT_TEMPLATE = (
    "SELECT ONCOTREE_CODE AS code, COUNT(*) AS row_count FROM ({union}) "
    "WHERE ONCOTREE_CODE IS NOT NULL AND TRIM(ONCOTREE_CODE) <> '' "
    "GROUP BY 1 ORDER BY row_count DESC, code"
)


def distinct_oncotree_sql(study_schemas: Iterable[str]) -> str:
    """Build the documented distinct-ONCOTREE_CODE enumeration SQL.

    ``study_schemas`` are cbioportal_* DuckDB schemas each exposing a
    ``sample`` table with an ``ONCOTREE_CODE`` column.
    """
    parts = [f"SELECT ONCOTREE_CODE FROM {s}.sample" for s in study_schemas]
    if not parts:
        raise ValueError("at least one study schema is required")
    return _DISTINCT_TEMPLATE.format(union=" UNION ALL ".join(parts))


def discover_oncotree_schemas(cursor: _Cursor) -> list[str]:
    """Return cbioportal_* schemas whose ``sample`` table has ONCOTREE_CODE."""
    cursor.execute(
        "SELECT DISTINCT table_schema FROM information_schema.columns "
        "WHERE column_name = 'ONCOTREE_CODE' AND table_name = 'sample' "
        "ORDER BY table_schema"
    )
    return [row[0] for row in cursor.fetchall()]


def enumerate_distinct_codes(cursor: _Cursor) -> list[tuple[str, int]]:
    """Enumerate (code, row_count) over every discovered study schema."""
    schemas = discover_oncotree_schemas(cursor)
    if not schemas:
        return []
    cursor.execute(distinct_oncotree_sql(schemas))
    return [(str(row[0]), int(row[1])) for row in cursor.fetchall()]
